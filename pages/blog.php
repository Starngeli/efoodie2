Never was this pattern of lukewarm response more clear than last week. That’s when my first lesson for better-for-you baking crystallized in my mind. The key is to make it sound delicious. I'd accidentally stumbled upon this formula with my Seeded Whole-Wheat Banana Bread recipe. As of this writing, the banana bread is the most Instagrammed recipe from my book, and I credit that to a common understanding of what banana bread is. It’s not hard to make the jump from, “Okay, this is what I like banana bread to taste like,” to “Oh, okay, all I have to do is swap out regular flour for whole wheat, and stir some seeds in.” It’s small steps like these that make healthyish eating sustainable, and it still tastes great.<3<br/>
